function m = retrieve3DMonomials(k)

if k == 1
    m = @(X) [ones(size(X,1),1) , ...
              X(:,1)              , ...   %   (1,0,0)
              X(:,2)               , ...   %   (0,1,0)
              X(:,3)               ];
          
        
elseif k == 2
              
    m = @(X) [ones(size(X,1),1) , ...
              X(:,1)              , ...   %   (1,0,0)
              X(:,2)               , ...   %   (0,1,0)
              X(:,3)               , ...   %   (0,0,1)
              X(:,1).^2          , ...   %   (2,0,0)
              X(:,1).*X(:,2) , ...   %   (1,1,0)
              X(:,1).*X(:,3), ...   %   (1,0,1)
              X(:,2).^2, ...   %   (0,2,0) 
              X(:,2).*X(:,3), ...   %   (0,1,1)
              X(:,3).^2];
          
end