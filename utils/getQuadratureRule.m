function [x, w, nq] = getQuadratureRule(degree, dim)

    switch dim
        case 1
            
            degree = degree + (degree == 0);
            l  = legendrePolynomials(degree);
            dl = cellfun(@(l) dx(l,1), l, 'unif', false);
            dl = dl{end};
            
            if degree <= 1
                x = 0;
                
            elseif degree == 2
                x = [-sqrt(1/3); sqrt(1/3)];
                
            elseif degree == 3
                x = [-sqrt(3/5); 0; sqrt(3/5)];
                
            elseif degree == 4
                x = [-sqrt(3/7 + 2/7*sqrt(6/5)); -sqrt(3/7 - 2/7*sqrt(6/5)); ...
                      sqrt(3/7 - 2/7*sqrt(6/5));  sqrt(3/7 + 2/7*sqrt(6/5))];
                
            end
            
            w = 2./((1 - x.^2).*dl(x).^2);
            
            x = (x + 1)/2;
            x = [x, 1-x];
            w = w/2;

        case 2
            
            if degree <= 1
                
                w = 1;
                
                x = [0,0];
            
            elseif degree == 2
                
                w = [1;
                     1;
                     1]/3;
                
                x = [0,1,1;
                     1,0,1;
                     1,1,0]/2;
                
            elseif degree == 3
                
                w = [-0.56250000000000000000;
                      0.52083333333333333333;
                      0.52083333333333333333;
                      0.52083333333333333333];
                  
                  
                x = [0.333333333333333   0.333333333333333   0.333333333333333;
                     0.200000000000000   0.600000000000000   0.200000000000000;
                     0.200000000000000   0.200000000000000   0.600000000000000;
                     0.600000000000000   0.200000000000000   0.200000000000000];
                
            elseif degree <= 4

                w = [0.22338158967801;
                     0.22338158967801;
                     0.22338158967801;
                     0.10995174365532;
                     0.10995174365532;
                     0.10995174365532];
                 
                x = [0.108103018168060   0.445948490915970   0.445948490915970;
                     0.445948490915960   0.445948490915970   0.108103018168070;
                     0.445948490915960   0.108103018168070   0.445948490915970;
                     0.816847572980460   0.091576213509770   0.091576213509770;
                     0.091576213509770   0.091576213509770   0.816847572980460;
                     0.091576213509770   0.816847572980460   0.091576213509770];
                 
%                 x = [0.44594849091597, 0.44594849091597;
%                      0.44594849091597, 0.10810301816807;
%                      0.10810301816807, 0.44594849091597;
%                      0.09157621350977, 0.09157621350977;
%                      0.09157621350977, 0.81684757298046;
%                      0.81684757298046, 0.09157621350977];
%                  
%                 xt = [0, 0; 1, 0; 0 1];
%                 
%                 T = [xt(1,:) - xt(3,:); xt(2,:) - xt(3,:)];
%                 l = (x - xt(3,:))/T;
%                 l = [l, 1-sum(l,2)];

            end
            
        case 3
            
            if degree <= 1
                
                w = 1;
                
                x = [0,0,0];
            
            elseif degree == 2
                
                a = 0.138196601125010515179541316563436;
                b = 1 - 3*a;
                
                w = [1;
                     1;
                     1;
                     1]/4;
                 
                x = [b a a a;
                     a b a a;
                     a a b a;
                     a a a b];
                 
            end
            
    end
    
    nq = numel(w);
    
end


